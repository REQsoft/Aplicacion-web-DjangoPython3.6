from rest_framework import serializers
from adminapp.models import Directorio, Localizacion, Articulo, Parametrizacion, Sql

# Serializer para servicios.
class ServicioSerializer(serializers.ModelSerializer):
    class Meta:
        model = Sql
        fields = ('nombre', 'rol')

# Serializer para directorio telefónico.
class DirectorioSerializer(serializers.ModelSerializer):
    class Meta:
        model = Directorio
        fields = ('dependencia', 'extension', 'linea_directa')

# Serializer para localización.
class LocalizacionSerializer(serializers.ModelSerializer):
    class Meta:
        model = Localizacion
        fields = ('descripcion', 'longitud', 'latitud')

# Serializer para artículos perdidos.
class ArticuloSerializer(serializers.ModelSerializer):
    class Meta:
        model = Articulo
        fields = ('descripcion', 'fecha', 'foto')

# Serializer para la parametrización del sistema.
class ParametrizacionSerializer(serializers.ModelSerializer):
    class Meta:
        model = Parametrizacion
        fields = ('nombre', 'lema', 'imagen', 'mision', 'vision', 'informacion', 'estado_directorio',
                  'estado_articulo', 'estado_localizacion', 'hoja_de_vida', 'oferta_academica', 'nota_semestre', 'informacion_materia', 'lista_estudiantes')
